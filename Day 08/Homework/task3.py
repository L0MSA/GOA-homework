# ასაკი
age = int(input("Enter your age: "))

# მეტია თუ არა 18ზე თუ ნაკლები ან ტოლი 20ზე
if age > 18 and age < 21:
    # თუ 18ს და 20ს შორისაა დაპრინტავს:
    print("Your age is more than 18 and less or equal than 20.")
else:
    # თუ არარის 18ის და 20ს შორის დაპრინტავს:
    print("Your age is not more than 18 and less than 20.")
