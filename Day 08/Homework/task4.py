# მაგალითი 1: >
x = 5
y = 3
if x > y:
    print("x is greater than y")

# მაგალითი 2: <
a = 10
b = 20
if a < b:
    print("a is less than b")

# მაგალითი 3: >=
p = 8
q = 8
if p >= q:
    print("p is greater than or equal to q")

# მაგალითი 4: <=
m = 15
n = 20
if m <= n:
    print("m is less than or equal to n")

# მაგალითი 5: !=
u = 10
v = 5
if u != v:
    print("u is not equal to v")

# მაგალითი 6: ==
s = 10
t = 10
if s == t:
    print("s is equal to t")
